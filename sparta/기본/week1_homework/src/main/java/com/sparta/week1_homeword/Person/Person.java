package com.sparta.week1_homeword.Person;

public class Person {

    private String name;
    private int age;
    private String address;

    public void setName(String name){
        this.name = name;
    }

    public void setAge(int age){
        this.age = age;
    }

    public void  setAddress(String address){
        this.address = address;
    }

    public String getName() {
        return this.name;
    }

    public int getAge() {
        return this.age;
    }

    public String getAddress() {
        return this.address;
    }
}
